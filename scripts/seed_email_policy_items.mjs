const newId = () => Math.random().toString(36).slice(2, 11);
const now = new Date().toISOString();

const post = async (body) => {
  const res = await fetch(`${API}/policyItems`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify(body)
  });
  if (!res.ok) {
    throw new Error(`${res.status} ${await res.text()}`);
  }
};

// 1) Parent group
const parentId = newId();
await post({
  id: parentId,
  policyId: POLICY_ID,
  parentId: null,
  order: 1,
  nameAr: 'البنود العامة',
  nameEn: 'General Items',
  descriptionAr: '',
  descriptionEn: '',
  createdAt: now,
  updatedAt: now
});
console.log(`✓ Parent: البنود العامة (${parentId})`);

// 2) 21 children
let ok = 0, fail = 0;
for (let i = 0; i < items.length; i++) {
  const num = i + 1;
  try {
    await post({
      id: newId(),
      policyId: POLICY_ID,
      parentId,
      order: num,
      nameAr: `البند 1-${num}`,
      nameEn: `Item 1-${num}`,
      descriptionAr: items[i],
      descriptionEn: '',
      createdAt: now,
      updatedAt: now
    });
    console.log(`✓ 1-${num}`);
    ok++;
  } catch (err) {
    console.error(`✗ 1-${num} — ${err.message}`);
    fail++;
  }
}
console.log(`\nDone. Inserted: ${ok}/21${fail ? `, Failed: ${fail}` : ''}`);
